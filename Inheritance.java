public class Inheritance{
    public static void main(String[] args) {
        VeryAdvCalci obj=new VeryAdvCalci();
        System.out.println(obj.add(2,4));
        System.out.println(obj.sub(4,2));
        System.out.println(obj.multi(2,4));
        System.out.println(obj.div(4,2));
        System.out.println(obj.power(2,3));
    }
}
